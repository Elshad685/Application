package utility;

import java.math.BigDecimal;

public class Utility {

    public static BigDecimal convertDoubleToBigDecimal(double a) {

        BigDecimal bd = new BigDecimal(a);
        return bd;
    }

    public static BigDecimal convertDoubleToInteger(int a) {

        BigDecimal bd = new BigDecimal(a);
        return bd;
    }
}
